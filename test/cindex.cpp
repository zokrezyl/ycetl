int main(int argc, char **argv) {

  const char *args[] = {
      "-resource-dir",
      "/home/linuxbrew/.linuxbrew/Cellar/llvm/20.1.6/lib/clang/20",
      "-Xclang",
      "-internal-isystem",
      "/home/linuxbrew/.linuxbrew/Cellar/llvm/20.1.6/lib/clang/20/include",
      "-Xclang",
      "-internal-isystem",
      "/usr/local/include",
      "-Xclang",
      "-internal-isystem",
      "/usr/lib/gcc/x86_64-linux-gnu/12/../../../../x86_64-linux-gnu/include",
      "-Xclang",
      "-internal-externc-isystem",
      "/usr/include/x86_64-linux-gnu",
      "-Xclang",
      "-internal-externc-isystem",
      "/include",
      "-Xclang",
      "-internal-externc-isystem",
      "/usr/include"};

  CXIndex index = clang_createIndex(0, 0); // Create index
  //
  size_t nargs = sizeof(args) / sizeof(args[0]);

  CXTranslationUnit unit = clang_parseTranslationUnit(
      index, argv[1], args, nargs, NULL, 0, CXTranslationUnit_None);

  if (unit == nullptr) {
    yinfo("Unable to parse translation unit.");
    return 0;
  }
  CXCursor cursor = clang_getTranslationUnitCursor(
      unit); // Obtain a cursor at the root of the translation unit

  CXString unit_spelling = clang_getTranslationUnitSpelling(unit);
  CXFile file = clang_getFile(unit, clang_getCString(unit_spelling));
  CXString abs = clang_File_tryGetRealPathName(file);
  std::string name = clang_getCString(abs);
  clang_disposeString(abs);
  clang_disposeString(unit_spelling);
  CIndex cindex(name); // Create an instance of CIndex
  clang_visitChildren(
      cursor,
      [](CXCursor current_cursor, CXCursor parent, CXClientData client_data) {
        auto *cindex = static_cast<CIndex *>(client_data);
        CXType cursor_type = clang_getCursorType(current_cursor);
        cindex->add_type(current_cursor, cursor_type);
        return CXChildVisit_Recurse;
      },
      &cindex);

  cindex.dump("out.hpp");
}
