std::string get_file_name(CXCursor cursor) {
  CXFile file;
  unsigned line, column, offset;
  // TODO check if the cursor is valid
  // check if source location is valid
  // check if the file is valid
  CXSourceLocation loc = clang_getCursorLocation(cursor);
  if (clang_Location_isFromMainFile(loc) == 0) {
    ydebug("Location is not from main file");
    return "";
  }
  clang_getExpansionLocation(loc, &file, &line, &column, &offset);
  CXString file_name = clang_getFileName(file);
  CXString abs = clang_File_tryGetRealPathName(file);

  std::string name = clang_getCString(abs);

  clang_disposeString(abs);
  clang_disposeString(file_name);
  return name;
}

struct Type {
  std::string name;  // Name of the type
  std::size_t size;  // Size of the type in bytes
  std::size_t align; // Alignment of the type in bytes
};

struct Field {
  std::string name;   // Name of the field
  std::string type;   // Type of the field
  std::size_t offset; // Offset of the field in the record
};

struct Record : public Type {
  std::vector<Field> fields; // Fields of the record
};

struct Enum : public Type {
  std::vector<std::pair<std::string, int>> values; // Values of the enum
  std::string underlying_type; // Underlying type of the enum
};

struct Typedef : public Type {};

struct FunctionProto : public Type {
  std::vector<std::string> arg_types; // Arguments of the function
};

/* function is not a type!, it has a type, FunctionProto*/
struct Function {
  std::string name;
  std::string type;
};

class CIndex {

  std::unordered_map<std::string, Enum> enums;
  std::unordered_map<std::string, Record> records;
  std::unordered_map<std::string, FunctionProto> function_protos;
  std::unordered_map<std::string, Typedef> typedefs;
  std::string unit; // The name of the translation unit

public:
  CIndex(std::string unit) : unit(unit) { ydebug("Cindex constructor called"); }
  void add_type(CXCursor cursor, CXType cursor_type) {

    CXSourceLocation loc = clang_getCursorLocation(cursor);
    if (clang_Location_isFromMainFile(loc) == 0) {
      return;
    }

    CXString type_spelling = clang_getTypeSpelling(cursor_type);
    CXString type_kind_spelling = clang_getTypeKindSpelling(cursor_type.kind);

    /*
    auto cursor_file = get_file_name(cursor);
    if (cursor_file != unit) {
      return;
    }
    */

    switch (cursor_type.kind) {
    case CXType_Enum:
      enums.insert(clang_getCString(type_spelling));
      break;
    case CXType_Record:
      records.insert(clang_getCString(type_spelling));
      break;
    case CXType_FunctionProto:
      functions.insert(clang_getCString(type_spelling));
      break;
    case CXType_Typedef:
      typedefs.insert(clang_getCString(type_spelling));
      break;
    case CXType_Pointer:
    case CXType_LValueReference:
    case CXType_RValueReference:
      add_type(cursor, clang_getPointeeType(cursor_type));
      break;
    }
    clang_disposeString(type_kind_spelling);
    clang_disposeString(type_spelling);
  }
  void dump(const char *filename);
};

